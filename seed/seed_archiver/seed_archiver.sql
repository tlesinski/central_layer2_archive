DECLARE
  l_count PLS_INTEGER;
BEGIN
  SELECT COUNT(*)
    INTO l_count
    FROM USER_TABLES
   WHERE TABLE_NAME IN ('TBL_ARCHIVER_TABLES', 'TBL_ARCHIVER_PARTITIONS', 'TBL_ARCHIVER_RUNS');

  IF l_count <> 3 THEN
    RAISE_APPLICATION_ERROR(-20410, 'ARCHIVER code is not installed in the configured schema');
  END IF;

  SELECT COUNT(*)
    INTO l_count
    FROM USER_DB_LINKS
   WHERE DB_LINK = UPPER('&&ACTIVE_AGENT_LINK');

  IF l_count = 0 THEN
    RAISE_APPLICATION_ERROR(-20411, 'ARCHIVER AGENT DB link does not exist: &&ACTIVE_AGENT_LINK');
  END IF;
END;
/

SELECT COUNT(*) FROM &&CLIENT1_SCHEMA..ORDERS_ARCH_SRC@&&ACTIVE_AGENT_LINK WHERE 1 = 0;
