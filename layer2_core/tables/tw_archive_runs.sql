CREATE TABLE TW_ARCHIVE_RUNS
(
  RUN_ID              NUMBER GENERATED BY DEFAULT AS IDENTITY,
  RUN_TYPE            VARCHAR2(30) NOT NULL,
  RUN_STATUS          VARCHAR2(30) DEFAULT 'INIT' NOT NULL,
  SOURCE_DB_LINK      VARCHAR2(128),
  SOURCE_OWNER        VARCHAR2(128),
  SOURCE_TABLE_NAME   VARCHAR2(128),
  MSTR_LOG_ID         NUMBER,
  STARTED_AT          TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  ENDED_AT            TIMESTAMP,
  REQUESTED_BY        VARCHAR2(128) DEFAULT SYS_CONTEXT('USERENV', 'SESSION_USER') NOT NULL,
  EXECUTE_FLAG        VARCHAR2(1) DEFAULT 'N' NOT NULL,
  ERROR_MESSAGE       VARCHAR2(4000),
  CREATED_AT          TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT          TIMESTAMP,
  CONSTRAINT TW_ARCHIVE_RUNS_PK PRIMARY KEY (RUN_ID),
  CONSTRAINT TW_ARCHIVE_RUNS_FK1 FOREIGN KEY
    (SOURCE_DB_LINK, SOURCE_OWNER, SOURCE_TABLE_NAME)
    REFERENCES TW_ARCHIVE_TABLES
    (SOURCE_DB_LINK, SOURCE_OWNER, SOURCE_TABLE_NAME),
  CONSTRAINT TW_ARCHIVE_RUNS_CK1 CHECK
    (RUN_TYPE IN ('DISCOVER', 'ARCHIVE', 'QUALITY', 'TRUNCATE', 'RUNNER')),
  CONSTRAINT TW_ARCHIVE_RUNS_CK2 CHECK
    (RUN_STATUS IN ('INIT', 'RUNNING', 'SUCCESS', 'WARNING', 'ERROR', 'CANCELLED')),
  CONSTRAINT TW_ARCHIVE_RUNS_CK3 CHECK (EXECUTE_FLAG IN ('Y', 'N'))
);

CREATE INDEX TW_ARCHIVE_RUNS_IX1
  ON TW_ARCHIVE_RUNS (RUN_TYPE, RUN_STATUS, STARTED_AT);

CREATE INDEX TW_ARCHIVE_RUNS_IX2
  ON TW_ARCHIVE_RUNS (SOURCE_DB_LINK, SOURCE_OWNER, SOURCE_TABLE_NAME);
